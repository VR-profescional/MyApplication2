package com.example.myapplication;

public class Event {
    public double years;
    public double days;
    public double minutes;
    public double hours;
    public double seconds;

    public Event(double years, double days, double minutes, double hours, double seconds) {
        this.years = years;
        this.days = days;
        this.minutes = minutes;
        this.hours = hours;
        this.seconds = seconds;
    }
}